package com.myhb.hibernate.pojo;

public class Gear {
	
	private Long id;
	private String name;
	
	public Gear() {}
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
